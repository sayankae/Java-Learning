package ThisPackage;

public class MainClass {
    //Default
    int a = 5;

    void someFunctionA() {
        System.out.println("This is default function.");
    }

    //Public
    public int b = 10;

    public void someFunctionB() {
        System.out.println("This is a public function.");
    }

    //Protected
    protected int c = 15;

    protected void someFunctionC() {
        System.out.println("This is a protected function.");
    }

    //Private
    private int d = 20;

    private void someFunctionD() {
        System.out.println("This is a private function.");
    }


}
